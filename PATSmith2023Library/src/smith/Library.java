    /**
     * Receives input regarding a book from employee, validates the input using
     * methods from the Book class, and returns error messages or inserts the
     * book into the Book table
     *
     * @param evt
     */
    private void btnAddBookActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAddBookActionPerformed
    {//GEN-HEADEREND:event_btnAddBookActionPerformed
        String title = txtTitleAdd.getText();
        String author = txtAuthorAdd.getText();
        String isbn = txtISBNAdd.getText();
        String year = txtYearAdd.getText();
        String genre = cmbGenre.getSelectedItem().toString();
        // boolean variables to use during validation
        boolean valid;
        boolean checkISBN;
        boolean validateISBN;
        boolean validateYear;

        // Presence check and check length of input
        if (title.equals("") || author.equals("") || isbn.equals("") || year.equals(""))
            JOptionPane.showMessageDialog(this, "All fields need to be filled in", "Error", 0);
        else if (isbn.length() != 13)
            JOptionPane.showMessageDialog(this, "ISBN needs to have 13 digits", "Error", 0);
        else if (year.length() != 4)
            JOptionPane.showMessageDialog(this, "Year entered is invalid", "Error", 0);
        else
        {
            // Create Book object and call methods to validate ISBN and year input
            Books book = new Books(author, title, year, isbn);
            validateISBN = book.validate(isbn);
            validateYear = book.validate(year);
            // Display error messages if input invalid
            if (!validateISBN)
                JOptionPane.showMessageDialog(this, "ISBN must only contain digits", "Error", 0);
            else if (!validateYear)
                JOptionPane.showMessageDialog(this, "Year must only contain digits", "Error", 0);
            else
            {
                // Call method from Book class to check if book already exists in database
                valid = book.bookInfo();
                // Call method from Book class to check if the ISBN already exists in the database
                checkISBN = book.checkISBN();
                // if the book already exists in the database, display error message
                if (valid)
                    JOptionPane.showMessageDialog(this, "Book already exists", "Error", 0);
                else if (checkISBN)
                    JOptionPane.showMessageDialog(this, "ISBN already exists in database", "Error", 0);
                else
                {
                    // Call method to insert book's details into Book table in database
                    book.insertBook(genre);
                    JOptionPane.showMessageDialog(this, "Book has been entered into database");
                }
            }
        }
    }//GEN-LAST:event_btnAddBookActionPerformed
