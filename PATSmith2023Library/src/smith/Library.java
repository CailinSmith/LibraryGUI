    /**
     * Search for a member using their username and ID Returns the username,
     * password, phone number and reserved book of member
     *
     * @param evt
     */
    private void btnAccessMemSearchActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAccessMemSearchActionPerformed
    {//GEN-HEADEREND:event_btnAccessMemSearchActionPerformed
        String username = txtAccessUsername.getText();
        String memID = txtAccessMemID.getText();

        // Presence check
        if (username.equals("") || memID.equals(""))
            JOptionPane.showMessageDialog(this, "Both fields must be filled in.", "Error", 0);
        else
        {
            // Creates Member object to use method to validate ID
            Member memAcc = new Member("", "");
            boolean validateID = memAcc.validateInput(memID);

            if (validateID)
            {
                // Creates new Member object to return the information of the member 
                // that matches the ID and username
                int idint = Integer.parseInt(memID);
                memAcc = new Member(username, idint);
                txaAccessMembers.setText(memAcc.accessMembers());
            }
            else
                JOptionPane.showMessageDialog(this, "ID must only contains digits.", "Error", 0);
        }
    }//GEN-LAST:event_btnAccessMemSearchActionPerformed
