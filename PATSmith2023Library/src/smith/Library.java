    /**
     * Receive book information input from member, send select query to Book
     * class, return results as a String and set to text area
     *
     * @param evt
     */
    private void btnSearchBookActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSearchBookActionPerformed
    {//GEN-HEADEREND:event_btnSearchBookActionPerformed
        String author = txtSearchAuthor.getText();
        String title = txtSearchTitle.getText();
        String year = txtSearchYear.getText();
        String isbn = txtSearchISBN.getText();

        boolean validateISBN;
        boolean validateYear;

        // Validate input - only digits, presence check, length of input
        boolean valid;
        if (author.equals("") && title.equals("") && year.equals("") && isbn.equals(""))
            JOptionPane.showMessageDialog(this, "Enter at least one field.", "Error", 0);
        else if (!year.equals("") && year.length() != 4)
            JOptionPane.showMessageDialog(this, "Year entered is invalid.", "Error", 0);
        else if (!isbn.equals("") && isbn.length() != 13)
            JOptionPane.showMessageDialog(this, "ISBN numbers must be 13 digits long.", "Error", 0);
        else
        {
            // Create Book object with information
            Books book = new Books(author, title, year, isbn);
            // Validate input
            validateISBN = book.validate(isbn);
            validateYear = book.validate(year);
            // Error messages for invalid input
            if (!validateISBN)
                JOptionPane.showMessageDialog(this, "ISBN must only contain digits.", "Error", 0);
            else if (!validateYear)
                JOptionPane.showMessageDialog(this, "Year must only contain digits.", "Error", 0);
            else
            {
                // Return results of sql query with book information
                valid = book.bookInfo();
                if (valid)
                    txaSearchResults.setText(book.getOutput());
                else
                    JOptionPane.showMessageDialog(this, "Book does not exist in database.", "Error", 0);
            }
        }
    }//GEN-LAST:event_btnSearchBookActionPerformed
