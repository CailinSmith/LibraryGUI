    /**
     * Receives input from the user, creates a Book object and reserved the book
     * on the member's account Input: author, title and ISBN of a book
     *
     * @param evt
     */
    private void btnTakeOutBookActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnTakeOutBookActionPerformed
    {//GEN-HEADEREND:event_btnTakeOutBookActionPerformed
        String author = txtReserveAuthor.getText();
        String title = txtReserveTitle.getText();
        String isbn = txtReserveISBN.getText();

        Books book = new Books(author, title, "", isbn);
        boolean valid;
        // Presence check
        if (author.equals("") || title.equals("") || isbn.equals(""))
            JOptionPane.showMessageDialog(this, "All fields must be filled in.", "Error", 0);
        else
        {
            // Checks if book exists in database
            valid = book.bookInfo();

            if (valid)
            {
                //If the book can be found in the database, Member object is created 
                Member memAcc = new Member("", id);
                // Method called to check if the member already has a book reserved
                boolean reserved = memAcc.checkReserved();
                if (reserved)
                    JOptionPane.showMessageDialog(this, "You already have a book reserved.\nReturn this book first.", "Error", 0);
                else
                {
                    // Inserts the member and the book's information into the MemberBook table
                    memAcc.insertReserve(isbn);
                    JOptionPane.showMessageDialog(this, "Book reserved successfully.");
                }
            }
            // Error message if book can not be found in the database
            else
                JOptionPane.showMessageDialog(this, "Book does not exist in database.\n"
                        + "Please check that all fields are inputted correctly.", "Error", 0);
        }
    }//GEN-LAST:event_btnTakeOutBookActionPerformed
    /**
     * Receives input from user, validates it, and updates the user's password
     * Input: Old password, new password twice - String
     *
     * @param evt
     */
    private void btnMemUpdatePassActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnMemUpdatePassActionPerformed
    {//GEN-HEADEREND:event_btnMemUpdatePassActionPerformed
        String oldPas = new String(pasMemOldPass.getPassword());
        String newPas = new String(pasMemNewPass.getPassword());
        String confirm = new String(pasMemPassConfirm.getPassword());

        // Presence check and checks if new password and confirm password input match
        boolean valid;
        if (oldPas.equals("") || newPas.equals("") || confirm.equals(""))
            JOptionPane.showMessageDialog(this, "All fields must be complete.", "Error", 0);
        else if (!newPas.equals(confirm))
            JOptionPane.showMessageDialog(this, "New password fields do not match.", "Error", 0);
        else
        {
            // Creates Member object using old password
            Member memAcc = new Member("", oldPas);
            // Sends the current member's ID
            memAcc.setID(id);
            // Checks whether the old password inputted is correct
            valid = memAcc.userInfo();
            if (valid)
            {
                // Calls method to update the member's password in the Members table
                memAcc.updatePassword(newPas);
                JOptionPane.showMessageDialog(this, "Password updated");
                frmEmployeePassword.setVisible(false);
                frmEmployeeHome.setVisible(true);
            }
            // Error message if incorrect old password is entered
            else
                JOptionPane.showMessageDialog(this, "Incorrect old password.", "Error", 0);
        }
    }//GEN-LAST:event_btnMemUpdatePassActionPerformed
