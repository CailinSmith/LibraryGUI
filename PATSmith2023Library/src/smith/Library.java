    /**
     * Received ISBN String input from user and validates it Creates Book
     * object, sending the ISBN Checks if book exists in database - Error
     * message or removes from database
     *
     * @param evt
     */
    private void btnDeleteBookActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDeleteBookActionPerformed
    {//GEN-HEADEREND:event_btnDeleteBookActionPerformed
        String isbn = txtDeleteISBN.getText();
        boolean valid;
        boolean validateISBN;
        // Presence check and length of input check
        if (isbn.equals(""))
            JOptionPane.showMessageDialog(this, "Please enter a ISBN", "Error", 0);
        else if (isbn.length() != 13)
            JOptionPane.showMessageDialog(this, "ISBN has to be 13 digits long", "Error", 0);
        else
        {
            // Compare ISBN to saved ISBN
            Books book = new Books("", "", "", isbn);
            // Check if ISBN only contains digits
            validateISBN = book.validate(isbn);
            if (!validateISBN)
                JOptionPane.showMessageDialog(this, "ISBN must only contain digits", "Error", 0);
            else
            {
                // Checks if book exists in database
                valid = book.bookInfo();
                if (valid)
                {
                    // Confirms with user whether they want to delete the book or not
                    int n = JOptionPane.showOptionDialog(this,
                            "Are you sure you want permanently to remove "
                            + book.bookDetails() + " from the database?",
                            "Confirm Deletion",
                            JOptionPane.YES_NO_OPTION,
                            JOptionPane.QUESTION_MESSAGE,
                            null,
                            options,
                            options[1]);
                    if (n == JOptionPane.YES_OPTION)
                    {
                        // Deletes the book from the database by calling deletBook method from Book class
                        book.deleteBook();
                        JOptionPane.showMessageDialog(this, "Book has been deleted");
                        txtDeleteISBN.setText("");
                    }
                }
                // Error message if book can not be found in the database
                else
                    JOptionPane.showMessageDialog(this, "Book does not exist.\nPlease make sure you entered the right ISBN.", "Error", 0);
            }
        }

    }//GEN-LAST:event_btnDeleteBookActionPerformed
